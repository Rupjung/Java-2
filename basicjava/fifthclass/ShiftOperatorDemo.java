package com.basicjava.fifthclass;

public class ShiftOperatorDemo {
	public static void main(String[] args) {
		
		int num1=33;
		int shiftLeftByOne=num1<<1;
		System.out.println(" Result "+num1+" <<1 "+shiftLeftByOne);
		
		int shiftRightByOne=num1<<2;
		System.out.println(" Result "+num1+" <<2 "+shiftRightByOne);
		
	}
}